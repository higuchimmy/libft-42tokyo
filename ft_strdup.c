#include "libft.h"

static char	*ft_strcpy(char *dest, const char *src)
{
	int i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

char		*ft_strdup(const char *src)
{
	char *dup;

	if (!(dup = (char *)malloc(sizeof(char) * (ft_strlen(src) + 1))))
		return (0);
	if (dup)
		ft_strcpy(dup, src);
	return (dup);
}
